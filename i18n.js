function getCurrentLang(){
  try {
    const l = localStorage.getItem('TOUR_LANG');
    if (l === 'en' || l === 'zh') return l;
  } catch(e){}
  return 'ja';
}

function setLang(lang){
  try { localStorage.setItem('TOUR_LANG', lang); } catch(e){}
  location.reload();
}

function t(key){
  const lang = getCurrentLang();
  const dict = UI_DICT[lang] || UI_DICT.ja;
  if (Object.prototype.hasOwnProperty.call(dict, key)) return dict[key];
  return (UI_DICT.ja[key] !== undefined) ? UI_DICT.ja[key] : key;
}

// data-i18n="key" を持つ要素のテキストを一括翻訳
function applyI18nStaticText(){
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = t(key);
    if (el.hasAttribute('data-i18n-html')) el.innerHTML = val;
    else el.textContent = val;
  });
}

// 言語切替UIのHTML（ヘッダー用・小型ピル3つ）
function langSwitcherHtml(extraStyle){
  const cur = getCurrentLang();
  const opts = [['ja','JA'], ['en','EN'], ['zh','繁中']];
  const btns = opts.map(([code,label]) =>
    `<button type="button" class="lang-switch-btn${code===cur?' active':''}" data-lang="${code}" onclick="setLang('${code}')">${label}</button>`
  ).join('');
  return `<div class="lang-switcher" style="${extraStyle||''}">${btns}</div>`;
}

// ヘッダー内の指定要素に言語切替UIを挿入
function mountLangSwitcher(containerId, extraStyle){
  const el = document.getElementById(containerId);
  if (el) el.innerHTML = langSwitcherHtml(extraStyle);
}
