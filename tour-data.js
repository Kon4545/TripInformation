// ── i18n resolver ──────────────────────────────────────────────
function isTranslatableNode(node) {
  if (!node || typeof node !== 'object' || Array.isArray(node)) return false;
  var keys = Object.keys(node);
  if (!keys.length) return false;
  var hasJa = false;
  for (var i = 0; i < keys.length; i++) {
    var k = keys[i];
    if (k === 'ja') { hasJa = true; continue; }
    if (k === 'en' || k === 'zh' || k === '_auto') continue;
    return false;
  }
  return hasJa && typeof node.ja === 'string';
}

function resolveLang(node, lang) {
  if (Array.isArray(node)) {
    var arr = [];
    for (var i = 0; i < node.length; i++) arr.push(resolveLang(node[i], lang));
    return arr;
  }
  if (node && typeof node === 'object') {
    if (isTranslatableNode(node)) {
      var v = node[lang];
      return (v !== undefined && v !== '') ? v : node.ja;
    }
    var out = {};
    for (var k in node) {
      if (Object.prototype.hasOwnProperty.call(node, k)) out[k] = resolveLang(node[k], lang);
    }
    return out;
  }
  return node;
}

function getCurrentLang() {
  try {
    var l = localStorage.getItem('TOUR_LANG');
    if (l === 'en' || l === 'zh') return l;
  } catch (e) {}
  return 'ja';
}

function getRawData() {
  try {
    var override = localStorage.getItem('TOUR_DATA_RAW_OVERRIDE');
    if (override) return JSON.parse(override);
  } catch (e) {}
  return TOUR_DATA;
}

function getData(lang) {
  var raw = getRawData();
  return resolveLang(raw, lang || getCurrentLang());
}
